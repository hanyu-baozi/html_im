pub type CaptchaStore = Arc<Mutex<HashMap<String, (String, chrono::DateTime<Utc>)>>>;

pub fn get_captcha_store() -> CaptchaStore {
    Arc::new(Mutex::new(HashMap::new()))
}

fn generate_captcha_code(length: usize) -> String {
    let chars: Vec<char> = "ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnpqrstuvwxyz23456789"
        .chars()
        .collect();
    let mut rng = thread_rng();
    (0..length)
        .map(|_| *chars.choose(&mut rng).unwrap())
        .collect()
}

// Generate captcha image as base64 PNG using simple pixel patterns
fn generate_captcha_image(code: &str, width: u32, height: u32) -> String {
    use std::io::Cursor;
    
    let mut pixels: Vec<u8> = vec![255; (width * height * 4) as usize];
    
    let mut rng = thread_rng();
    
    // Background with random noise
    for i in (0..pixels.len()).step_by(4) {
        let noise: u8 = rng.gen_range(230..255);
        pixels[i] = noise;     // R
        pixels[i + 1] = noise; // G
        pixels[i + 2] = noise.saturating_add(rng.gen_range(0..5)); // B
        pixels[i + 3] = 255;   // A
    }
    
    // Draw noise lines
    for _ in 0..4 {
        let color: [u8; 3] = [
            rng.gen_range(180..220),
            rng.gen_range(180..220),
            rng.gen_range(180..220),
        ];
        let x1 = rng.gen_range(0..width) as i32;
        let y1 = rng.gen_range(0..height) as i32;
        let x2 = rng.gen_range(0..width) as i32;
        let y2 = rng.gen_range(0..height) as i32;
        
        let dx = (x2 - x1).abs();
        let dy = -(y2 - y1).abs();
        let mut err = dx + dy;
        let sx = if x1 < x2 { 1 } else { -1 };
        let sy = if y1 < y2 { 1 } else { -1 };
        let mut cx = x1;
        let mut cy = y1;
        
        loop {
            if cx >= 0 && cx < width as i32 && cy >= 0 && cy < height as i32 {
                let idx = ((cy as u32 * width + cx as u32) * 4) as usize;
                if idx + 3 < pixels.len() {
                    pixels[idx] = color[0];
                    pixels[idx + 1] = color[1];
                    pixels[idx + 2] = color[2];
                }
            }
            if cx == x2 && cy == y2 { break; }
            let e2 = 2 * err;
            if e2 >= dy { err += dy; cx += sx; }
            if e2 <= dx { err += dx; cy += sy; }
        }
    }
    
    // Draw characters using simple bitmap patterns
    let char_count = code.chars().count();
    let char_width = (width - 16) / char_count as u32;
    let cell_w = 8u32;
    let cell_h = 12u32;
    let scale_x = (char_width / cell_w).max(1);
    let scale_y = ((height - 8) / cell_h).max(1);
    
    for (i, ch) in code.chars().enumerate() {
        let pattern = get_char_pattern(ch);
        let offset_x = rng.gen_range(0..3);
        let offset_y = rng.gen_range(0..3);
        let rotation = rng.gen_range(-15..15) as f32;
        
        let color: [u8; 3] = [
            rng.gen_range(40..140),
            rng.gen_range(40..140),
            rng.gen_range(40..140),
        ];
        
        let base_x = 8 + i as u32 * char_width + (char_width - cell_w * scale_x) / 2 + offset_x;
        let base_y = (height - cell_h * scale_y) / 2 + offset_y;
        
        let cos_a = rotation.to_radians().cos();
        let sin_a = rotation.to_radians().sin();
        let cx = (cell_w as f32 * scale_x as f32) / 2.0;
        let cy = (cell_h as f32 * scale_y as f32) / 2.0;
        
        for py in 0..cell_h {
            for px in 0..cell_w {
                if pattern[py as usize][px as usize] == 1 {
                    for sy in 0..scale_y {
                        for sx in 0..scale_x {
                            // Apply rotation around center
                            let lx = px as f32 * scale_x as f32 + sx as f32 + 0.5 - cx;
                            let ly = py as f32 * scale_y as f32 + sy as f32 + 0.5 - cy;
                            let rx = (lx * cos_a - ly * sin_a + cx) as i32;
                            let ry = (lx * sin_a + ly * cos_a + cy) as i32;
                            
                            let fx = (base_x as i32 + rx) as u32;
                            let fy = (base_y as i32 + ry) as u32;
                            
                            if fx < width && fy < height {
                                let idx = ((fy * width + fx) * 4) as usize;
                                if idx + 3 < pixels.len() {
                                    pixels[idx] = color[0];
                                    pixels[idx + 1] = color[1];
                                    pixels[idx + 2] = color[2];
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    
    // Add noise dots
    for _ in 0..(width * height / 30) {
        let x = rng.gen_range(0..width);
        let y = rng.gen_range(0..height);
        let idx = ((y * width + x) * 4) as usize;
        if idx + 3 < pixels.len() {
            let color: u8 = rng.gen_range(120..200);
            pixels[idx] = color;
            pixels[idx + 1] = color;
            pixels[idx + 2] = color;
        }
    }
    
    // Encode as PNG
    let mut buf = Vec::new();
    {
        let mut encoder = png::Encoder::new(Cursor::new(&mut buf), width, height);
        encoder.set_color(png::ColorType::Rgba);
        encoder.set_depth(png::BitDepth::Eight);
        let mut writer = encoder.write_header().unwrap();
        writer.write_image_data(&pixels).unwrap();
    }
    
    general_purpose::STANDARD.encode(&buf)
}

// Verify captcha code
pub fn verify_captcha_code(store: &CaptchaStore, captcha_id: &str, code: &str) -> bool {
    let mut store = store.lock().unwrap();
    let now = Utc::now();
    
    // Remove expired captchas
    store.retain(|_, (_, expires_at)| *expires_at > now);
    
    match store.remove(captcha_id) {
        Some((stored_code, _)) => stored_code.eq_ignore_ascii_case(code),
        None => false,
    }
}

