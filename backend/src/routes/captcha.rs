pub async fn get_captcha(
    req: HttpRequest,
    store: web::Data<CaptchaStore>,
) -> HttpResponse {
    let code = generate_captcha_code(4);
    let captcha_id = Uuid::new_v4().to_string();
    let image_base64 = generate_captcha_image(&code, 200, 80);
    
    let expires_at = Utc::now() + chrono::Duration::minutes(5);
    store.lock().unwrap().insert(captcha_id.clone(), (code, expires_at));
    
    HttpResponse::Ok().json(serde_json::json!({
        "captcha_id": captcha_id,
        "image": format!("data:image/png;base64,{}", image_base64)
    }))
}

pub async fn verify_captcha(
    store: web::Data<CaptchaStore>,
    body: web::Json<serde_json::Value>,
) -> HttpResponse {
    let captcha_id = match body.get("captcha_id").and_then(|v| v.as_str()) {
        Some(id) => id.to_string(),
        None => {
            return HttpResponse::BadRequest().json(serde_json::json!({
                "error": "missing captcha_id"
            }));
        }
    };
    
    let code = match body.get("code").and_then(|v| v.as_str()) {
        Some(c) => c.to_string(),
        None => {
            return HttpResponse::BadRequest().json(serde_json::json!({
                "error": "missing code"
            }));
        }
    };
    
    if verify_captcha_code(&store, &captcha_id, &code) {
        HttpResponse::Ok().json(serde_json::json!({
            "valid": true
        }))
    } else {
        HttpResponse::BadRequest().json(serde_json::json!({
            "valid": false,
            "error": "invalid or expired captcha"
        }))
    }
}